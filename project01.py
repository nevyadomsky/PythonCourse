number1 = int(input("Enter a number 1: "))
number2 = int(input("Enter a number 2: "))
operation = input("Enter operation: ")

if operation == "+":
    print(number1 + number2)
elif operation == "-":
    print(number1 - number2)
elif operation == "*":
    print(number1 * number2)
elif operation == "/":
    print(number1 / number2)

